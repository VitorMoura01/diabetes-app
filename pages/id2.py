import streamlit as st

st.title("Fatores de Risco para Diabetes")

st.write("""
Além dos fatores genéticos e a ausência de hábitos saudáveis, existem outros fatores de risco que podem contribuir para o desenvolvimento do diabetes.
""")

st.subheader("Fatores de Risco:")
st.markdown("""
- **Diagnóstico de pré-diabetes**
- **Pressão alta**
- **Colesterol alto ou alterações na taxa de triglicérides no sangue**
- **Sobrepeso**, principalmente se a gordura estiver concentrada em volta da cintura
- **Pais, irmãos ou parentes próximos com diabetes**
- **Doenças renais crônicas**
- **Mulher que deu à luz criança com mais de 4kg**
- **Diabetes gestacional**
- **Síndrome de ovários policísticos**
- **Diagnóstico de distúrbios psiquiátricos** - esquizofrenia, depressão, transtorno bipolar
- **Apneia do sono**
- **Uso de medicamentos da classe dos glicocorticoides**
""")

st.write("""
_É importante estar atento a esses fatores de risco e buscar orientação médica se algum deles estiver presente._
""")
