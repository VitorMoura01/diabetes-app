import streamlit as st

st.title("Pé Diabético")

st.header("O que é o Pé Diabético?")
st.write("""
O pé diabético é uma série de alterações que podem ocorrer nos pés de pessoas com diabetes não controlado. Infecções ou problemas na circulação dos membros inferiores estão entre as complicações mais comuns, provocando o surgimento de feridas que não cicatrizam e infecções nos pés. **Se não for tratado, o pé diabético pode levar à amputação.**
""")

st.header("Sintomas")
st.write("""
– Formigamento\n
– Perda da sensibilidade local\n
– Dores\n
– Queimação nos pés e nas pernas\n
– Sensação de agulhadas\n
– Dormência\n
– Fraqueza nas pernas\n
""")
st.write("""
Tais sintomas podem piorar à noite, ao deitar. Normalmente, a pessoa só se dá conta quando está num estágio avançado e quase sempre com uma ferida ou uma infecção, o que torna o tratamento mais difícil devido aos problemas de circulação.
""")

st.header("Prevenção")
st.write("""
– **Examinar os pés diariamente** em um lugar bem iluminado. Quem não tiver condições de fazê-lo, precisa pedir a ajuda de alguém para verificar a existência de frieiras, cortes, calos, rachaduras, feridas ou alterações de cor. Uma dica é usar um espelho para ter uma visão completa. Nas consultas, pedir ao médico que examine os pés e avisá-lo imediatamente sobre eventuais alterações.

– **Manter os pés sempre limpos** e usar sempre água morna, nunca quente, para evitar queimaduras. A toalha deve ser macia. É melhor não esfregar a pele. Mantenha a pele hidratada, mas sem passar creme entre os dedos ou ao redor das unhas.

– **Usar meias sem costura**; o tecido deve ser algodão ou lã; evitar sintéticos, como nylon.

– **Antes de cortar as unhas**, lavá-las e secá-las bem. Para cortar, usar um alicate apropriado ou uma tesoura de ponta arredondada. O corte deve ser quadrado, com as laterais levemente arredondadas, e sem tirar a cutícula. Recomenda-se evitar idas a manicures ou pedicures, dando preferência a um profissional treinado, como o podólogo, o qual deve ser avisado do diabetes. O ideal é não cortar os calos, nem usar lixas. É melhor conversar com o médico sobre a possível causa do aparecimento dos calos.

– **Não andar descalço**. Manter os pés sempre protegidos, inclusive na praia e na piscina.

– **Escolher calçados ideais**: fechados, macios, confortáveis e com solados rígidos, que ofereçam firmeza. Antes de adquiri-los, olhar com atenção para ver se há deformação. As mulheres devem dar preferência a saltos quadrados, que tenham, no máximo, 3 cm de altura. Evitar sapatos apertados, duros, de plástico, de couro sintético, com ponta fina, saltos muito altos e sandálias que deixam os pés desprotegidos. Além disso, recomenda-se a não utilização de calçados novos, por mais de uma hora por dia, até que estejam macios. Os danos nos nervos podem causar mudanças na forma dos pés e dos dedos. Perguntar ao médico sobre sapatos terapêuticos especiais, ao invés de insistir e forçar o uso de sapatos comuns.
""")

st.header("Tratamento")
st.write("""
A abordagem deve ser especializada e deve contemplar um modelo de atenção integral:
- Educação
- Qualificação do risco
- Investigação adequada
- Tratamento apropriado das feridas
- Cirurgia especializada
- Aparelhamento correto
- Reabilitação global

O objetivo é a prevenção e a restauração funcional da extremidade afetada.
""")
