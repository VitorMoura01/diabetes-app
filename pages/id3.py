import streamlit as st

st.title("Uso da Insulina")

st.header("Etapas do Preparo de Um Tipo de Insulina na Seringa")

st.write("""
1. **Lavar e secar as mãos.**
2. Reunir a insulina, a seringa, o algodão e o álcool 70% líquido.
3. Homogeneizar a suspensão de insulina.
4. Fazer a assepsia da borracha do frasco de insulina.
5. Manter o protetor da agulha e aspirar o ar até a graduação correspondente à dose de insulina prescrita.
6. Retirar o protetor da agulha e injetar o ar no frasco de insulina.
7. Sem retirar a agulha, posicionar o frasco de cabeça para baixo e aspirar a insulina até a dose prescrita.
8. Eliminar bolhas de ar, se ocorrerem.
9. Virar o frasco para a posição inicial.
10. Remover a agulha do frasco, protegendo-a até o momento da aplicação.
""")

st.header("Etapas do Preparo de Dois Tipos de Insulina na Mesma Seringa")

st.write("""
**Insulinas NPH e Regular/Ultrarrápida**

1. Fazer a assepsia da borracha dos frascos de insulina.
2. Aspirar, na seringa, ar correspondente à dose de insulina NPH.
3. Injetar o ar no frasco de insulina NPH e depois retirar a agulha do frasco sem aspirar a insulina NPH.
4. Aspirar, na seringa, ar correspondente à dose de insulina Regular.
5. Injetar o ar no frasco de insulina Regular, virar o frasco e aspirar a dose prescrita de insulina Regular.
6. Colocar o frasco de insulina Regular na posição inicial e retirar a agulha.
7. Posicionar o frasco de insulina NPH de cabeça para baixo, introduzir a agulha da seringa, que já está com a insulina regular, e aspirar a dose correspondente à insulina NPH. O total de insulina na seringa deve corresponder à soma das doses das duas insulinas.
8. Retornar o frasco à posição inicial.
9. Remover a agulha do frasco, protegendo-a até o momento da aplicação.
10. **Se a dose aspirada na seringa for maior que a soma das doses prescritas, o excesso não deve ser devolvido ao frasco. É necessário, então, descartar a seringa com a insulina e reiniciar o procedimento com uma seringa nova.**
""")

st.header("Recomendações para Aplicação de Insulina")

st.write("""
- Inspecionar e palpar o local de aplicação antes de cada injeção de insulina, para detectar alterações na pele e/ou tecido subcutâneo.
- **Não aplicar a insulina em locais com lipohipertrofia até que o tecido se restabeleça, o que pode demorar meses a anos.**
- Ao mudar o local das injeções, é preciso alertar o paciente para possível variação glicêmica, necessitando de monitorização da glicemia intensificada e revisão da prescrição médica.
- Examinar o local de aplicação e o comprimento da agulha a ser usada para definir a realização ou não da prega subcutânea.
- A prega subcutânea deve ser feita elevando-se a pele, preferencialmente com dois ou três dedos. A pinça formada pelos dedos deve ser pressionada levemente para não impedir a acomodação da insulina injetada, evitando assim causar desconforto e machucar a pele.
- **Manter a agulha no tecido subcutâneo após a aplicação de insulina colabora para que toda a dose seja injetada, impede o refluxo de insulina no local da injeção e a saída pela agulha após a retirada.**
- Usando caneta, manter o botão injetor pressionado até a retirada total da agulha do subcutâneo para prevenir refluxo de sangue para o interior do refil reservatório com insulina. No caso de seringa, recomenda-se manter o êmbolo pressionado e a agulha no tecido subcutâneo.
- **O tempo de manutenção da agulha no tecido subcutâneo deve ser revisto sempre que houver saída de insulina após a injeção na ponta da agulha e/ou local da aplicação, podendo ser inferior a dez segundos.**
""")

st.header("Etapas da Aplicação de Insulina com Seringa")

st.write("""
1. Fazer assepsia com álcool 70% líquido no local escolhido para aplicação e esperar secar.
2. Fazer a prega subcutânea.
3. Introduzir a agulha com movimento único, rápido, firme e leve.
4. Injetar insulina continuamente, mas não de modo muito rápido.
5. Manter a agulha no tecido subcutâneo, com o êmbolo pressionado.
6. Soltar a prega subcutânea e remover a agulha suavemente, com movimento único.
7. Realizar suave pressão local, por alguns segundos, caso ocorra sangramento.
8. **Descartar o material em recipiente próprio.**
""")

st.header("Onde Descartar")

st.write("""
**Os resíduos devem ser descartados, imediatamente após o uso, em um coletor apropriado para resíduos perfurocortantes, biológicos e químicos.** O coletor é um recipiente rígido e inquebrável, com boca larga e tampa.
""")
